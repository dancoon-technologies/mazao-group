# Match trailing "  price  price  price" so we can strip to get product name
PRICE_TAIL_RE = re.compile(r"\s+[\d,]+\.\d{2}\s+[\d,]+\.\d{2}\s+[\d,]+\.\d{2}\s*$")
NAME_MAX_LENGTH = 120


def parse_product_names(text: str):
    """Yield (name,) for each product line. Name trimmed and truncated to NAME_MAX_LENGTH."""
    for line in text.strip().splitlines():
        line = line.strip()
        if not line:
            continue
        # Strip the three price columns from the end
        m = PRICE_TAIL_RE.search(line)
        if not m:
            continue
        name = line[: m.start()].strip()
        if not name or name.upper() in ("PRODUCTS", "DISTRIBUTOR", "WHOLESALE", "RETAIL", "PRICE-KES", "PRICE- KES", "RETAIL PRICE-", "KES"):
            continue
        if len(name) > NAME_MAX_LENGTH:
            name = name[:NAME_MAX_LENGTH]
        yield name


def seed_agriprice_products(apps, schema_editor):
    Department = apps.get_model("accounts", "Department")
    Product = apps.get_model("visits", "Product")

    department, _ = Department.objects.get_or_create(
        slug="agriprice",
        defaults={"name": "AgriPrice"},
    )

    seen = set()
    for name in parse_product_names(PRICELIST_TEXT):
        # Avoid duplicates (e.g. "SP WIND SHIELDER N16/SPRAY GUARD" appears twice with different prices)
        if name in seen:
            continue
        seen.add(name)
        Product.objects.get_or_create(
            department=department,
            name=name,
            defaults={"code": "", "unit": ""},
        )


